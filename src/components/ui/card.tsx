import * as React from "react"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const cardVariants = cva(
  "rounded-lg border text-card-foreground",
  {
    variants: {
      variant: {
        default: "bg-card shadow-sm",
        glass: `
          relative
          overflow-hidden
          bg-white/80 dark:bg-gray-900/80
          backdrop-blur-xl
          border-gray-200/50 dark:border-gray-800/50
          shadow-lg shadow-black/5
          hover:shadow-xl hover:-translate-y-1
          transition-all duration-300
          rounded-2xl
        `,
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
)

export interface CardProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof cardVariants> {}

const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(cardVariants({ variant }), className)}
      {...props}
    />
  )
)
Card.displayName = "Card"

// Decorative orb component for glass cards
export function CardDecorativeOrb({ color = "phoenix" }: { color?: 'phoenix' | 'gold' | 'green' | 'blue' }) {
  const colorClass = {
    phoenix: "bg-phoenix-500/5",
    gold: "bg-gold-500/5",
    green: "bg-green-500/5",
    blue: "bg-blue-500/5",
  }[color]
  
  return (
    <div className={cn(
      "absolute -right-6 -top-6 w-28 h-28 rounded-full blur-3xl pointer-events-none",
      colorClass
    )} />
  )
}

